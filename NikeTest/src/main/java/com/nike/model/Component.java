package com.nike.model;

public class Component {
	
	private String componentName;
	private String componentVal;
	private int componentIntVal;
	
	public String getComponentName() {
		return componentName;
	}
	public void setComponentName(String componentName) {
		this.componentName = componentName;
	}
	public String getComponentVal() {
		return componentVal;
	}
	public void setComponentVal(String componentVal) {
		this.componentVal = componentVal;
	}
	public int getComponentIntVal() {
		return componentIntVal;
	}
	public void setComponentIntVal(int componentIntVal) {
		this.componentIntVal = componentIntVal;
	}

}
